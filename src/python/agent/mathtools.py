def add_numbers(a: int, b: int) -> int:
    """
    Returns the sum of two numbers.

    Args:
        a: first number
        b: second number

    Returns:
        The sum of a and b
    """
    return a + b

def multiply_numbers(a: int, b: int) -> int:
    """
    Returns the product of two numbers.

    Args:
        a: first number
        b: second number

    Returns:
        The product of a and b
    """
    return a * b
